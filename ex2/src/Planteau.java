

public class Planteau
{
    private int cordX;
    private int cordY;
    

    public Planteau(int x, int y){
        cordX = x;
        cordY = y;
        
    }
    
    public int getCordX(){
        return cordX;
    }
    
     public int getCordY(){
        return cordY;
    }
    
    public void setCordX(int x){
        cordX = x;
    }
    
    public void setCordY(int y){
        cordY = y;
    }
    

 
}
